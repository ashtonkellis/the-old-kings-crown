const _allCards: Map<string, CardDef> = new Map();

(['nobility', 'clans', 'uprising', 'gathering'] as Faction[]).forEach(f => {
  buildBasicCards(f).forEach(c => _allCards.set(c.id, c));
  ADVANCED_CARDS[f].forEach(c => _allCards.set(c.id, c));
  HQ_CARDS[f].forEach(c => _allCards.set(c.id, c));
});

export function getCardDef(id: string): CardDef {
  const def = _allCards.get(id);
  if (!def) throw new Error(`Unknown card def: ${id}`);
  return def;
}
